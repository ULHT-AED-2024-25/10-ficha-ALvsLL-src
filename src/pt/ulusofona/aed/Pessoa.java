package pt.ulusofona.aed;

class Pessoa {

    String nome;
    String apelido;
    int nrBI;
    String paisMorada;


    public Pessoa() {
    }


    public Pessoa(String nome, String apelido, int nrBi, String paisMorada) {
        this.nome = nome;
        this.apelido = apelido;
        this.nrBI = nrBi;
        this.paisMorada = paisMorada;
    }

}
